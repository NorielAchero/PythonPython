# Type Casting is the process of converting a variable to another

name = "Noriel"
age = 22
grade = 1.15
is_student = False

print(type(name))

# int()

grade = int(grade)

print(f"{grade }")

# float()

age = float(age)

print(age)

# str()

age = str(age)

print(age)

# bool()

name = bool(name)

print(name) # Prints False if the value of string is ""

